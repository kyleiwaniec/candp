         		<h3>About The National Alliance</h3>
            <p>Creating, developing, and delivering practical continuing education programs for everyone involved in the insurance and risk management industries is the challenge and purpose of The National Alliance for Insurance Education &amp; Research.</p>
            <p>More than 142,000 participants are attending our 2,500+ programs, conducted in all 50 states and throughout the Caribbean.</p>

                    <p class="listTitle">Good branding is:</p>
                    <ul><li>Unique
                    </li><li>Memorable
                    </li><li>Pertinent
                    </li></ul>

                    <p class="listTitle">Our thoughts on logo design:</p>
                    <p>A logo must be simple and doesn’t have to convey what your company does. Don’t try to cram meaning into your logo with too many symbols. A simple logo design allows for easy recognition and allows the logo to be versatile & memorable. Good logos feature something unexpected or unique without being overdrawn. Think Apple, or Nike.
                    </p>
                    <p>A Logo should be:</p>
                    <ul>
                    <li>	<strong>Memorable:</strong>
                    <br />	Following closely behind the principle of simplicity, is that of memorability. An effective logo design should be memorable and this is achieved by having a simple, yet, appropriate logo.

                    </li><li>	<strong>Your logo is for your audience:</strong>
                    <br />	How you position the logo should be appropriate for its intended purpose. Appeal to your customers. You may not be the most conservative individual on the planet, but your corporate law firm client probably is. And what suits a Gym, probably doesn’t suit an Ice Cream Shop.

                    </li><li>	<strong>Timeless:</strong>
                    <br />	An effective logo should be timeless – that is, it will stand the test of time. Will the logo still be effective in 10, 20, 50 years?

                    </li><li>	<strong>Versatile:</strong>
                    <br />	An effective logo should be able to work across a variety of mediums and applications. For this reason a logo should be designed in vector format, to ensure that it can be scaled to any size. The logo must work in just one color too.
                    </li></ul>
